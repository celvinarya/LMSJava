    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        if(!(registFirstName.getText().trim().isEmpty()||registLastName.getText().trim().isEmpty()||registUsername.getText().trim().isEmpty()||(registPassword.getText().isEmpty()) ||(registPassword2.getText().isEmpty()))){
            Pattern p = Pattern.compile("[^a-z ]", Pattern.CASE_INSENSITIVE);
            Matcher m = p.matcher(registFirstName.getText()+registLastName.getText());
            boolean b = m.find();
            if (b){
                JOptionPane.showMessageDialog(this, "Please enter a string in the name field!");
            }
            else{
                Pattern q = Pattern.compile("[^a-z0-9 ]", Pattern.CASE_INSENSITIVE);
                Matcher n = q.matcher(registUsername.getText());
                boolean a = n.find();
                if (a){
                    JOptionPane.showMessageDialog(this, "Invalid value in username fields! Please input in string or number");
                }else{
        //            Cek apakah username sudah digunakan
                    int usersCount = DatabaseOperations.getUsers("SELECT * FROM users WHERE username = '"+registUsername.getText().trim()+"'").size();
        //            End CEK
                    if(usersCount > 0){
                        JOptionPane.showMessageDialog(this, "Username has been used! Please choose another username.");
                    }else{
        //                CEK APAKAH KEDUA PASSWORD COCOK
                        if(registPassword.getText().equals(registPassword2.getText())){
                            User addnewuser = new User();
                            addnewuser.setFirstName(registFirstName.getText().trim());
                            addnewuser.setLastName(registLastName.getText().trim());
                            addnewuser.setUsername(registUsername.getText().trim());
                            addnewuser.setPassword(registPassword.getText());
                            addnewuser.setUser_type("General");
                            DatabaseOperations.addUser(addnewuser.getFirstName(), addnewuser.getLastName(), addnewuser.getUsername(), addnewuser.getPassword(), addnewuser.getUser_type());
                            JOptionPane.showMessageDialog(this, "User added successfully!");
                            resetLoginPanel();
                            removePanel();
                            addPanel(loginPanel);
                        }else{
                            JOptionPane.showMessageDialog(this, "Password doesn't match!");
                        }
                    }
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Please fill in all fields");
        }
    }//GEN-LAST:event_signUpActionPerformed

