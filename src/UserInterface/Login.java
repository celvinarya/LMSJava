    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!(usernameLoginText.getText().isEmpty() || passwordLoginText.getText().isEmpty())){
//            Cek Username ada atau tidak
            try{
                String query="SELECT * FROM users WHERE username = '"+usernameLoginText.getText()+"'";
                String username=DatabaseOperations.getUsers(query).get(0).getUsername();
                String statusUser = DatabaseOperations.getUsers(query).get(0).getUser_type();
                String password_temp = DatabaseOperations.getUsers(query).get(0).getPassword();
                if(passwordLoginText.getText().equals(password_temp)){
                    if(statusUser.equals("Librarian")){
                        Librarian librarian = new Librarian(username);
                        librarian.setVisible(true);
                        this.setVisible(false);
                    }else if (statusUser.equals("General")){
                        General general = new General(username);
                        general.setVisible(true);
                        this.setVisible(false);
                    }
                }else{
                    JOptionPane.showMessageDialog(this, "Wrong password!");
                }
            }catch(IndexOutOfBoundsException e){
                JOptionPane.showMessageDialog(this, "Invalid username!");
            }
//            END
        }else{
            JOptionPane.showMessageDialog(this, "Please fill in all fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

